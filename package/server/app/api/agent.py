@router.delete("/messages", summary="删除会话的消息")
def delete_messages(
    session_id: str = Query(..., description="The ID of the session"),
    message_ids: str = Query(None, description="Comma-separated list of message IDs to delete"),
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db)
):
    """
    删除指定会话的全部或部分消息。
    """
    session = agent_crud.get_session(db, session_id)
    if not session:
        raise HTTPException(status_code=404, detail="Session not found")
    if str(session.user_id) != str(current_user.id):
        raise HTTPException(status_code=403, detail="Not authorized to modify this session")
        
    if message_ids:
        ids_list = [int(i.strip()) for i in message_ids.split(",") if i.strip().isdigit()]
        if ids_list:
            from app.db.models.agent import AgentMessage as DBAgentMessage
            db.query(DBAgentMessage).filter(
                DBAgentMessage.session_id == session.id,
                DBAgentMessage.id.in_(ids_list)
            ).delete(synchronize_session=False)
            db.commit()
    else:
        success = agent_crud.delete_messages_by_session(db, session_id)
    return {"message": "Messages deleted successfully"}
